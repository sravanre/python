marks = [4,6,7,8,9,3,4,5,6,7,8,8]

#list created with marks

#prints from index 2 to index 5
print(marks[2:6])

#print from starting till index 5
print(marks[:6])

# print from index 2 till last
print(marks[2:])

#print all the list
print(marks[:])

#print from index 1 to 9 and step of 2
print(marks[1:10:2])


#print from starting till end with step of 2 
print(marks[::2])


#reversing the list 
text = "hello"
print(text[::-1])
